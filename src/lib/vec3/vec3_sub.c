#include "vec3.h"

/*
 * vec3_sub - 두 3차원 벡터의 뺄셈 연산
 * @v1: 첫 번째 벡터 (피감수)
 * @v2: 두 번째 벡터 (감수)
 *
 * 두 벡터의 각 성분을 빼서 새로운 벡터를 반환합니다.
 * 기하학적으로 v2에서 v1으로 향하는 벡터를 계산합니다.
 *
 * 계산: result = v1 - v2 = (v1.x - v2.x, v1.y - v2.y, v1.z - v2.z)
 *
 * 레이트레이싱에서 두 점 사이의 방향 벡터를 구할 때 주로 사용됩니다.
 * 예: 광원에서 물체로의 방향 = 물체위치 - 광원위치
 *
 * Return: 뺄셈 결과 벡터
 */
t_vec3	vec3_sub(t_vec3 v1, t_vec3 v2)
{
	t_vec3	result;

	result.x = v1.x - v2.x;
	result.y = v1.y - v2.y;
	result.z = v1.z - v2.z;
	return (result);
}
