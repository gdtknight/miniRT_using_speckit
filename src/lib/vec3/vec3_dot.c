#include "vec3.h"

/*
 * vec3_dot - 두 3차원 벡터의 내적(Dot Product) 연산
 * @v1: 첫 번째 벡터
 * @v2: 두 번째 벡터
 *
 * 두 벡터의 내적을 계산합니다.
 * 내적 = v1.x * v2.x + v1.y * v2.y + v1.z * v2.z
 * 내적 = |v1| * |v2| * cos(θ) (θ는 두 벡터 사이의 각)
 *
 * 내적의 의미:
 * - 양수: 두 벡터가 같은 방향
 * - 0: 두 벡터가 수직
 * - 음수: 두 벡터가 반대 방향
 *
 * Return: 내적 결과 (스칼라 값)
 */
double	vec3_dot(t_vec3 v1, t_vec3 v2)
{
	return (v1.x * v2.x + v1.y * v2.y + v1.z * v2.z);
}
